#creating a list 
list=["abhi",12,23,34,354,65,'a',"legend"]
#append():adding of an element in in list
list.append("kamalesh")
print(list)
#extend(:adding an list into the list)
list.extend(['a','b','h','i'])
print(list)
#remove():removing an element in the list 
list.remove("abhi")
print(list)
#insert():insert an element in the list at perticular position of the list
list.insert(0,"geetha")
print(list)
#type():to check this is list or not
(type(list))
#list.clear()
#print(list)
list.pop()
print(list)
#reverse():printing the elements of the list in the reverse way
list.reverse()
print(list)
#copy():copy the list
list.copy()
print(list)
#sorting():sorting of the elements in the list
list.sort()
print(list)