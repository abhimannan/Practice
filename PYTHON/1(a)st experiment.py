#python program to demonstrate different types of datatypes in python
#integer datatype
x=12
print(x)
print(type(x))
#float datatype
x=12.4
print(x)
print(type(x))
#complex datatype
x=complex(2,3)
print(x)
print(type(x))
#binary
x=0o11
print(x)
print(oct(x))
print(hex(x))
#AND,OR,NOT
a=5
b=2
print(bin(a&b))
print(bin(a|b))
print(bin(a^b))
print(a<<2)
print(b>>4)
